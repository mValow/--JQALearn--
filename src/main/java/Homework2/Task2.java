package Homework2;

public class Task2 {
    public static void main(String[] args) {

        int num1 = 100;
        int num2 = 90;
        System.out.println("Arithmetically average of numbers 100 and 90 is: " + average(num1, num2));
    }

    public static int average(int n, int n1) {
        int averageResult = (n + n1) / 2;
        return averageResult;

    }
}
